package com.example.lvyou.bean;

public class PlaceBean {
    private int placenum;
    private String placename;
    private String place;
    private int star;
    private float ticket;
    private String placeother;

    public int getPlacenum() {
        return placenum;
    }

    public void setPlacenum(int placenum) {
        this.placenum = placenum;
    }

    public String getPlacename() {
        return placename;
    }

    public void setPlacename(String placename) {
        this.placename = placename;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public int getStar() {
        return star;
    }

    public void setStar(int star) {
        this.star = star;
    }

    public float getTicket() {
        return ticket;
    }

    public void setTicket(float ticket) {
        this.ticket = ticket;
    }

    public String getPlaceother() {
        return placeother;
    }

    public void setPlaceother(String placeother) {
        this.placeother = placeother;
    }
}
