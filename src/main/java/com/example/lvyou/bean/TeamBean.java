package com.example.lvyou.bean;

public class TeamBean {
    private int teamnum;
    private String teamname;
    private String teamphone;
    private int teamrnum;
    private String teamother;

    public int getTeamnum() {
        return teamnum;
    }

    public void setTeamnum(int teamnum) {
        this.teamnum = teamnum;
    }

    public String getTeamname() {
        return teamname;
    }

    public void setTeamname(String teamname) {
        this.teamname = teamname;
    }

    public String getTeamphone() {
        return teamphone;
    }

    public void setTeamphone(String teamphone) {
        this.teamphone = teamphone;
    }

    public int getTeamrnum() {
        return teamrnum;
    }

    public void setTeamrnum(int teamrnum) {
        this.teamrnum = teamrnum;
    }

    public String getTeamother() {
        return teamother;
    }

    public void setTeamother(String teamother) {
        this.teamother = teamother;
    }
}
